    private void LoadData(){
        try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã Nhân Viên");
        model.addColumn("Tên Nhân Viên");
        model.addColumn("Giới Tính");
        model.addColumn("SĐT");
        model.addColumn("Ngày Vào Làm");
        model.addColumn("Tên Đăng Nhập");
        model.addColumn("Mật Khẩu");
        model.addColumn("Mã Chức Vụ");
        
        
        List<NhanVien> NhanVienList = NhanVienDAO.getDanhSachNhanVien();

        // Thêm dữ liệu từ danh sách Sản Phẩm vào model
        for (NhanVien NhanVien : NhanVienList) {
            Object[] row = {
              NhanVien.getMaNV(),
              NhanVien.getTenNV(),
               NhanVien.getGioiTinh(),
              NhanVien.getSDT(),
               NhanVien.getNgayVaoLam(),
              NhanVien.getTenDangNhap(),
               NhanVien.getMatKhau(),
              NhanVien.getIdRole(),
              
            };
            model.addRow(row);
        }

        // Set model cho jTableSanPham
        jTable2.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
        txt_GioiTinh.setText("");
        txt_maChucVu.setText("");
        txt_maNhanVien.setText("");
        txt_matKhau.setText("");
        txt_ngayVaoLam.setText("");
        txt_sdt.setText("");
        txt_tenDangNhap.setText("");
        txt_tenNhanVien.setText("");
    }
