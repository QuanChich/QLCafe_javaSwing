    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
         int selectedRow = jTable2.getSelectedRow();

        if (selectedRow != -1) {
            String MaNV = (String) jTable2.getValueAt(selectedRow, 0);
            String TenNV = (String) jTable2.getValueAt(selectedRow, 1);
            String GioiTinh = (String) jTable2.getValueAt(selectedRow, 2);
            String SDT = (String) jTable2.getValueAt(selectedRow, 3);
            LocalDateTime ngayVaoLamLocalDateTime = (LocalDateTime) jTable2.getValueAt(selectedRow, 4);
            String TenDangNhap = (String) jTable2.getValueAt(selectedRow, 5);
            String MatKhau = (String) jTable2.getValueAt(selectedRow, 6);
            int IdRole = (int) jTable2.getValueAt(selectedRow, 7);
           
            Date NgayVaoLam = Date.from(ngayVaoLamLocalDateTime.atZone(ZoneId.systemDefault()).toInstant());
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String ngayVaoLamString = dateFormat.format(NgayVaoLam);
            


            // Gán giá trị vào các ô text
            txt_maNhanVien.setText(MaNV);
            txt_tenNhanVien.setText(TenNV);
            txt_GioiTinh.setText(GioiTinh);
            txt_sdt.setText(SDT);
            txt_ngayVaoLam.setText(ngayVaoLamString);
            txt_tenDangNhap.setText(TenDangNhap);
            txt_matKhau.setText(MatKhau);
            txt_maChucVu.setText(String.valueOf(IdRole));
            
        }
    }//GEN-LAST:event_jTable2MouseClicked

