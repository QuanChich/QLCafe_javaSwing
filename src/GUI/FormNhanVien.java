    LocalDateTime ngayVaoLam = LocalDateTime.parse(ngayVaoLamString, formatter);
    NhanVien nhanVien = new NhanVien(txt_maNhanVien.getText(), txt_tenNhanVien.getText(), txt_GioiTinh.getText(), txt_sdt.getText(), ngayVaoLam, txt_tenDangNhap.getText(), txt_matKhau.getText(), Integer.parseInt(txt_maChucVu.getText()));
