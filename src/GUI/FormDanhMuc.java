    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();

        if (selectedRow != -1) {
            String MaDanhMuc = (String) jTable1.getValueAt(selectedRow, 0);
            String TenDanhMuc = (String) jTable1.getValueAt(selectedRow, 1);
           
            // Gán giá trị vào các ô text
            txt_maDanhMuc.setText(MaDanhMuc);
            txt_tenDanhMuc.setText(TenDanhMuc);
        }
    }//GEN-LAST:event_jTable1MouseClicked

