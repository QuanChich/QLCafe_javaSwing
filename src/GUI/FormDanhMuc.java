    private void LoadData(){
        try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã Danh Mục");
        model.addColumn("Tên Danh Mục");
        
        List<DanhMuc> DanhMucList = DanhMucDAO.getDanhSachDanhMuc();

        // Thêm dữ liệu từ danh sách Sản Phẩm vào model
        for (DanhMuc danhMuc : DanhMucList) {
            Object[] row = {
              danhMuc.getMaDanhMuc(),
              danhMuc.getTenDanhMuc()
            };
            model.addRow(row);
        }

        // Set model cho jTableSanPham
        jTable1.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
        txt_maDanhMuc.setText("");
        txt_tenDanhMuc.setText("");
    }
